package PatternPrograms;

public class Pattern_program_12 {
	 public static void main(String[] args) {

		 	    for (int row = 5; row >= 1; row-- )
		 	    {
		 	        for(int column = 5; column >= row; column--) {
		 	            
		 	                System.out.print("* ");
		 	            
		 	        }//close 1st for loop
		 	        System.out.println();
		 	    }//close 2nd for loop
		 	}//main close

		 }//class close
